//---------------------------------------------------------------------------

#pragma hdrstop
#pragma package(smart_init)
#include "CImagen.h"

//---------------------------------------------------------------------------


Imagen::Imagen(BYTE* dibujo, int ancho, int alto)
{
  m_dibujo=dibujo;
  m_ancho= ancho;
  m_alto;
}

//---------------------------------------------------------------------------

Imagen::~Imagen()
{
 delete [] m_dibujo;
}

//---------------------------------------------------------------------------

int Imagen::GetAncho()
{
 return m_ancho;
}

//---------------------------------------------------------------------------

int Imagen::GetAlto()
{
 return m_alto;
}

//---------------------------------------------------------------------------

BYTE* Imagen::GetDibujo()
{
 return m_dibujo;
}


